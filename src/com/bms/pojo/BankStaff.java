package com.bms.pojo;

public class BankStaff {

	private int staffId;
	private String staffName,departmentName,staffRule,staffPassword,staffEmailId,staffAddress;
	private long staffSalary,staffContact;
	
	public int getStaffId() {
		return staffId;
	}
	public void setStaffId(int staffId) {
		this.staffId = staffId;
	}
	public String getStaffName() {
		return staffName;
	}
	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}
	public String getDepartmentName() {
		return departmentName;
	}
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}
	public String getStaffRule() {
		return staffRule;
	}
	public void setStaffRule(String staffRule) {
		this.staffRule = staffRule;
	}
	public String getStaffPassword() {
		return staffPassword;
	}
	public void setStaffPassword(String staffPassword) {
		this.staffPassword = staffPassword;
	}
	public String getStaffEmailId() {
		return staffEmailId;
	}
	public void setStaffEmailId(String staffEmailId) {
		this.staffEmailId = staffEmailId;
	}
	public String getStaffAddress() {
		return staffAddress;
	}
	public void setStaffAddress(String staffAddress) {
		this.staffAddress = staffAddress;
	}
	public long getStaffSalary() {
		return staffSalary;
	}
	public void setStaffSalary(long staffSalary) {
		this.staffSalary = staffSalary;
	}
	public long getStaffContact() {
		return staffContact;
	}
	public void setStaffContact(long staffContact) {
		this.staffContact = staffContact;
	}
}
